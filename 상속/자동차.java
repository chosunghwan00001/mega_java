package 상속;

public class 자동차 {
	String color;
	String name;
	
	public void start() {
		
		System.out.println("출발하다");
	}
	
	public void stop() {
		System.out.println("멈추다");
	}

	@Override
	public String toString() {
		return "자동차 [color=" + color + ", name=" + name + "]";
	}
}
